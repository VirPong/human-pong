<h1>&#9612; code of conduct &#9616;</h1>


<p>The Code of Conduct outlines the rules that you need to follow to help ensure that VirPong is fun for everyone.</p>

<p><h3>I. You may do the following:</h3>
	<ul>
		<li>Be polite and treat others with respect. Just because you're online doesn't mean you should be a jerk.</li>
		<li>Welcome newcomers. The more people on the Services, the more fun for everyone.</li>
		<li>Know your privacy settings and use them to help create and control the experience you want. If you're a parent, you can use Family Settings to control the activities and experiences available to your kid. For example, you can prohibit voice and video communication, block purchasing, and choose who your kid is allowed to play with online.</li>
		<li>Make friends.</li>
		<li>Have fun!</li>
	</ul>
</p>

